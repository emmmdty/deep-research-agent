"""研究系统各 Agent 使用的提示词模板。"""

from __future__ import annotations

from datetime import datetime


def get_current_date() -> str:
    """返回当前日期的可读字符串。"""
    return datetime.now().strftime("%Y年%m月%d日")


# ============================================================
# Planner Agent — 研究规划专家
# ============================================================

PLANNER_SYSTEM_PROMPT = """\
你是一名深度研究规划专家。你的职责是将用户的研究主题拆解为 3-5 个互补且可执行的子任务。

核心原则：
- 子任务之间避免重复，覆盖主题的不同维度
- 从基础概念到前沿应用，逻辑递进
- 每个子任务都要有明确的研究意图和搜索查询
- 搜索查询应当精准，可混用中英文以获取更全面的结果
"""

PLANNER_USER_PROMPT = """\
当前日期：{current_date}
研究主题：{research_topic}

请将该主题分解为 3-5 个研究子任务，严格以 JSON 格式输出：
{{
  "tasks": [
    {{
      "title": "子任务标题",
      "intent": "研究意图说明",
      "query": "搜索引擎查询语句"
    }}
  ]
}}

注意：只输出 JSON，不要包含其他文本。
"""


